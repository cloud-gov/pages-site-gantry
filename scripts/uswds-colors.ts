// Helper to get color value from token name and shade
export function getUSWDSColor(token: string, shade: string = '60'): string {
  const isVivid = token.endsWith('-vivid');
  const shadeKey = isVivid ? `${shade}v` : shade;
  const colorKey = `${token}-${shadeKey}`;
  
  return uswdsColors[colorKey] || '#000000';
}
